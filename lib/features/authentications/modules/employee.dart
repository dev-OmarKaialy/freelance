
class Employee {
  String id;
  String name;
  String companyName;
  String position;
  String email;
  String phoneNumber;
  String address;

  Employee({
    required this.id,
    required this.name,
    required this.companyName,
    required this.position,
    required this.email,
    required this.phoneNumber,
    required this.address,
  });
}